//1.Math.abs()
let absValue= Math.abs(2-3);
console.log('abs:', absValue);

// 2.Math.ceil()
let  ceilValue= Math.ceil(0.60);
console.log('Ceil:',  ceilValue);

// 3.Math.floor()
let floorValue = Math.floor(5.6);
console.log('Floor:', floorValue);

// 4.Math.round()
const roundValue = Math.round(15.4);
console.log('Round:', roundValue);

// 5.Math.Max()
const maxNumber = Math.max(1056, 200, 44430, 6740,100);
console.log('Max:', maxNumber);

// 6.Math.min()
const minNumber = Math.min(2.5,142350, 202524, 345450, 2540);
console.log('Min:', minNumber);

// 7.Math.pow()
const powerValue = Math.pow(2, 4); 
console.log('Power:', powerValue);

// 8.Math.sqrt()
const sqrtValue = Math.sqrt(225);
console.log('Square root:', sqrtValue);

// 9.Math.trunc() 
const truncValue = Math.trunc(1.13);
console.log('Truncate:', truncValue);

// 10.Math.random()
const randomNumber = Math.random();
console.log('Random number:', randomNumber);

